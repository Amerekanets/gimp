static Argument *
layer_new_invoker (ProcRecord   *proc_record,
                   Gimp         *gimp,
                   GimpContext  *context,
                   GimpProgress *progress,
                   Argument     *args)
{
  gboolean success = TRUE;
  Argument *return_vals;
  GimpImage *image;
  gint32 width;
  gint32 height;
  gint32 type;
  gchar *name;
  gdouble opacity;
  gint32 mode;
  GimpLayer *layer = NULL;

  image = gimp_image_get_by_ID (gimp, args[0].value.pdb_int);
  if (! GIMP_IS_IMAGE (image))
    success = FALSE;

  width = args[1].value.pdb_int;
  if (width < 1)
    success = FALSE;

  height = args[2].value.pdb_int;
  if (height < 1)
    success = FALSE;

  type = args[3].value.pdb_int;
  if (type < GIMP_RGB_IMAGE || type > GIMP_INDEXEDA_IMAGE)
    success = FALSE;

  name = (gchar *) args[4].value.pdb_pointer;
  if (name && !g_utf8_validate (name, -1, NULL))
    success = FALSE;

  opacity = args[5].value.pdb_float;
  if (opacity < 0.0 || opacity > 100.0)
    success = FALSE;

  mode = args[6].value.pdb_int;
  if (mode < GIMP_NORMAL_MODE || mode > GIMP_COLOR_ERASE_MODE)
    success = FALSE;

  if (success)
    {
      layer = gimp_layer_new (image, width, height, type, name,
                              opacity / 100.0, mode);

      if (! layer)
        success = FALSE;
    }

  return_vals = procedural_db_return_values (proc_record, success);

  if (success)
    return_vals[1].value.pdb_int = layer ? gimp_item_get_ID (GIMP_ITEM (layer)) : -1;

  return return_vals;
}

